     */
    public function store(Request $request)
    {
        $validator = Validator::make($request->all(), $this->rules);

        if ($validator->fails())
        {   
            return back()->withErrors($validator)
                         ->withInput();
        }
        else
        {
            $mail = new Mail();
            $mail->name = $request->name;
            $mail->html = implode('', file($request->file));
            $mail->save();

